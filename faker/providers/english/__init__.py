    @classmethod
    def en_word(cls):
        """
        Generate a random word
        :example 'lorem'
        """
        return cls.random_element(cls.words)

    @classmethod
    def en_line(cls):
        """
        Generate an array of random words
        :example array('Lorem', 'ipsum', 'dolor')
        :param nb how many words to return
        """
        return cls.random_element(cls.sentences) + '.'

    @classmethod
    def en_paragraph(cls):
        nb = cls.randomize_nb_elements()
        lines = [cls.en_line() for i in range(0, nb)]
        return ' '.join(lines)
